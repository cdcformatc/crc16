def init_crc16_tab( ):
    crc_tab16 = []
    for i in range(256):
        crc = 0;
        c = i;

        for j in range(8):
            if ( (crc ^ c) & 0x0001 ):
                crc = ( crc >> 1 ) ^ 0xA001
            else:
                crc =   crc >> 1
            c = c >> 1;

        crc_tab16.append(crc);
    
    return crc_tab16
    
def crc16(x):
    uCRCHi = 0x00ff
    uCRCLo = 0x00ff

    for i in range(0,len(x),2):
        c1 = x[i]
        c2 = x[i+1]
        
        c = int(c1+c2,16)
    
        uIndex = uCRCHi ^ c
        uCRCHi = uCRCLo ^ auCRCHi[uIndex & 0x00ff] ;
        uCRCLo = auCRCLo[uIndex & 0x00ff] ;
    
    return (uCRCHi << 8 | uCRCLo) ;

def update_crc_16(crc,c):
    short_c = 0x00ff &  c;
    tmp =  (crc ^ short_c) & 0xff;
    crc = (crc >> 8) ^ pre_crc_tab16[tmp];
    return crc
    
